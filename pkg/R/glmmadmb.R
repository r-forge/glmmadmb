"par_read" <-
function (name) 
{
    n = nchar(name)
    if (substring(name, n - 3, n) == ".par") 
        file_name = name
    else file_name = paste(name, ".par", sep = "")
    tmp = scan(file_name, what = "", quiet = TRUE)
    tmp2 = split(tmp, cumsum(tmp == "#"))
    x = tmp2[-1]
    for (i in 1:length(x)) {
        y = x[[i]]
        n = nchar(y[2])
        x[[i]] = as.numeric(y[-(1:2)])
        names(x)[i] = substring(y[2], 1, n - 1)
    }
    x$loglik = -as.numeric(tmp2[[1]][11])
    x$gradient = -as.numeric(tmp2[[1]][16])
    x
}
"pin_write" <-
function (name, L) 
{
    n = nchar(name)
    if (substring(name, n - 3, n) == ".pin") 
        file_name = name
    else file_name = paste(name, ".pin", sep = "")
    cat("# \"", name, ".pin\" produced by pin_write() from ADMButils; ", 
        date(), "\n", file = file_name, sep = "")
    for (i in 1:length(L)) {
        x = L[[i]]
        if (data.class(x) == "numeric") 
            cat("#", names(L)[i], "\n", L[[i]], "\n\n", file = file_name, 
                append = TRUE)
        if (data.class(x) == "matrix") {
            cat("#", names(L)[i], "\n", file = file_name, append = TRUE)
            write.table(L[[i]], , col = FALSE, row = FALSE, quote = FALSE, 
                file = file_name, append = TRUE)
            cat("\n", file = file_name, append = TRUE)
        }
    }
}
